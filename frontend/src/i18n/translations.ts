export function formatTranslation(
  template: string,
  params: Record<string, string | number>,
): string {
  return Object.entries(params).reduce((result, [key, value]) => {
    return result.replace(new RegExp(`\\{${key}\\}`, "g"), String(value));
  }, template);
}
